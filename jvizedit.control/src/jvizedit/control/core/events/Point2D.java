package jvizedit.control.core.events;

public class Point2D {

	private final double x;
	private final double y;

	public Point2D(final double x, final double y) {
		this.x = x;
		this.y = y;
	}

	public double getX() {
		return this.x;
	}

	public double getY() {
		return this.y;
	}
}
